CREATE TABLE user_role (
  user_id VARCHAR2(30 BYTE) NOT NULL,
  role_id VARCHAR2(30 BYTE) NOT NULL,
  PRIMARY KEY (user_id,role_id),
 CONSTRAINT fk_user_role_roleid FOREIGN KEY (role_id) REFERENCES ROLE(ID),
