CREATE TABLE USERS(
  ID VARCHAR2(30 BYTE) NOT NULL ENABLE,
  USERNAME VARCHAR2(255 BYTE),
  password VARCHAR2(255 BYTE),
  PRIMARY KEY (ID)
);



